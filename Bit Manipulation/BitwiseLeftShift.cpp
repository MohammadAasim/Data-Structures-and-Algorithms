#include <bits/stdc++.h>
using namespace std;
int main()
{
    cout<<" 5<<1 : "<<(5<<1)<<endl;
    //5=5<<1 == 5=5*2 
    cout<<" 5<<2 : "<<(5<<2)<<endl;
    //5=5<<2 == 5=5*4 
    cout<<" 5<<3 : "<<(5<<3)<<endl;
    //5=5<<3 == 5=5*8
    cout<<" 5<<4 : "<<(5<<4)<<endl;
    //5=5<<4 == 5=5*16
    return 0;
}