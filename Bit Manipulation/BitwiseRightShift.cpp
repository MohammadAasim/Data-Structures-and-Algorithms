#include <bits/stdc++.h>
using namespace std;
int main()
{
    cout<<" 10>>1 : "<<(10>>1)<<endl;
    //10>>1 == 10=10/2^1
    cout<<" 10>>2 : "<<(10>>2)<<endl;
    //10>>2 == 10=10/2^2 
    cout<<" 10>>3 : "<<(10>>3)<<endl;
    //10>>3 == 10=10/2^3
    cout<<" 10>>4 : "<<(10>>4)<<endl;
    //10>>4 == 10=10/2^4
    return 0;
}